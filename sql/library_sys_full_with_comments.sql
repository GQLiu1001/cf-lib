  description   TEXT DEFAULT NULL COMMENT '简介/描述',

  created_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  created_by    BIGINT DEFAULT NULL COMMENT '创建人用户ID',
  updated_by    BIGINT DEFAULT NULL COMMENT '更新人用户ID',
  deleted_at    DATETIME DEFAULT NULL COMMENT '删除时间（软删除标记）',
  version       INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',

  KEY idx_book_title (title),
  KEY idx_book_isbn (isbn),
  KEY idx_book_category (category_id),
  KEY idx_book_deleted (deleted_at),
  CONSTRAINT fk_book_category FOREIGN KEY (category_id) REFERENCES lib_category(id)
